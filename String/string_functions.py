name = "pranta nag"

print(len(name))            # print length

print(name.endswith("ta"))                  # return false. Because last two word is ta.

print(name.startswith("pr"))                  # return True. Because first two word is pr.

print(name.capitalize())                    # capital first character

print(name.upper())                         # Converts all characters to uppercase

print(name.lower())                          # Converts all characters to lowercase

print(name.isalpha())                       # Checks if all characters are alphabets

print(name.isdigit())                       # Checks if all characters are digits

print(name.split())                         # Splits string into a list

print(name.find("t"))                       # Returns the first index of a substring or -1

print(name.rstrip())                        # Removes trailing whitespaces

print(name.replace("pranta", "Joy"))        # Replaces all occurrences of a substring









