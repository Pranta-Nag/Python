marks = [47.8, 47.2, 77.3, 73.6, 58.9]
print(marks)    # print all list
print(type(marks))      # print list type
print(marks[3])         # print index number 3
print(marks[2])         # print index number 2
print(len(marks))       # print list length
print(marks[2:4])       # positive slicing
print(marks[-4:-1])     # negative slicing


