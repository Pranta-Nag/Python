import math

# square root
print(math.sqrt(16))

# power
print(math.pow(4,2))

# trigonometric fraction
print(math.tan(math.radians(45)))
print(math.sin(45))
print(math.sin(math.radians(45)))

# logarithmic functions
print(math.log(10))
print(math.log10(5))

# factorial
print(math.factorial(5))

# absolute value
print(math.fabs(-5.5))

# floor & ceiling
print(math.ceil(7.7))
print(math.floor(4.7))

# constant function
print(math.pi)
print(math.e)

# GCM: greatest common division
num1 = 30
num2 = 20
num3 = 10

print(math.gcd(num1,num2,num3))

# LCM: least common multiple
print(math.lcm(num1,num2,num3))


